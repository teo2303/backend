export class User {
    constructor( first_name, last_name, user_name, age, email, password, role ) {
        this.first_name = first_name
        this.last_name  = last_name
        this.user_name  = user_name
        this.age        = age
        this.email      = email
        this.password   = password
        this.role       = role
    }
}